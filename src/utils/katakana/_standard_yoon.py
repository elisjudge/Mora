k_yoon = ["キャ","キュ","キョ"]
s_yoon = ["シャ","シュ","ショ"]
t_yoon = ["チャ","チュ","チョ"]
n_yoon = ["ニャ","ニュ","ニョ"]
h_yoon = ["ヒャ","ヒュ","ヒョ"]
m_yoon = ["ミャ","ミュ","ミョ"]
r_yoon = ["リャ","リュ","リョ"]

def get_standard_yoon():
    return (k_yoon + s_yoon + t_yoon +
            n_yoon + h_yoon + m_yoon +
            r_yoon)
