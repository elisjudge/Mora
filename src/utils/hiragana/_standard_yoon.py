k_yoon = ["きゃ","きゅ","きょ"]
s_yoon = ["しゃ","しゅ","しょ"]
t_yoon = ["ちゃ","ちゅ","ちょ"]
n_yoon = ["にゃ","にゅ","にょ"]
h_yoon = ["ひゃ","ひゅ","ひょ"]
m_yoon = ["みゃ","みゅ","みょ"]
r_yoon = ["りゃ","りゅ","りょ"]


def get_standard_yoon():
    return (k_yoon + s_yoon + t_yoon +
            n_yoon + h_yoon + m_yoon +
            r_yoon)
